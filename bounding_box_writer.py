def draw_bounding_box(image_path, co_ords_meta):
    image_name = image_path.split('/')[-1]
    readenImage = cv2.imread(image_path)
    for roi_value in co_ords_meta:
        print(readenImage.shape)
        x1 = roi_value[0]
        y1 = roi_value[1]
        x2 = roi_value[2]
        y2 = roi_value[3]
        color = (0, 255, 0)
        thickness = 2
        x1, y1, x2, y2 = round(x1), round(y1), round(x2), round(y2)
        readenImage = cv2.rectangle(readenImage, (x1, y1), (x2, y2), color, thickness)
        cv2.imwrite(f'D:/utilitys/{image_name}_image_with_bbox.jpg', readenImage)
    

